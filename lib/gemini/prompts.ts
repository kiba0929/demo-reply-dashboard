export const RECIPIENT_MESSAGES = {
  professor: 'お疲れ様です。ちょっといいですか？',
  family: 'お疲れ様。ちょっといい？',
  friend: 'お疲れ！ちょっといい！？'
} as const;

export type RecipientType = keyof typeof RECIPIENT_MESSAGES;

// ===== 送信者の表示名を取得 =====
function getSenderDisplayName(recipient: RecipientType): string {
  switch (recipient) {
    case 'professor': return '教授';
    case 'family': return '両親';
    case 'friend': return '友人';
    default: return '送信者';
  }
}

