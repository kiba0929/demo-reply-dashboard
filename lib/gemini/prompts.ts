// ===== 抽象化返信プロンプト（第2段階） =====
export function buildReplyFromAbstractionPrompt(
  transformedActivity: string,
  recipient: RecipientType
): string {
  const senderName = getSenderDisplayName(recipient);

  const replyFormats = {
    professor: {
      honorifics: '謙譲語（です・ます + おり/ござい）',
      format: '「XXXしておりまして、返信が遅れそうです。申し訳ございません。」形式',
      examples: [
        '作業に集中しておりまして、返信が遅れそうです。申し訳ございません。',
        '所用で外出しておりまして、返信が遅れそうです。すみません。',
        '資料の確認をしておりまして、返信が遅れそうです。申し訳ございません。'
      ]
    },
    family: {
      honorifics: '丁寧語（です・ます）+ ややカジュアル',
      format: '「XXXしてて、返信遅れそう。ごめんなさい。」形式',
      examples: [
        '外出してて、返信遅れそう。ごめんなさい。',
        '家のことしてて、返信遅れそう。ごめんね。',
        '休んでて、返信遅れそう。すみません。'
      ]
    },
    friend: {
      honorifics: 'カジュアル（タメ口）',
      format: '「XXXしてるから返信遅れそう。ごめん。」形式',
      examples: [
        'ゲームしてるから返信遅れそう。ごめん。',
        '勉強してるから返信遅れそう。悪い。',
        '外出してるから返信遅れそう。ごめんね。'
      ]
    }
  };

  const replyFormat = replyFormats[recipient];

  return `以下の行動説明を使って、受信者（${senderName}）に適した敬語レベルで返信が遅れそうな理由を説明するメッセージを生成してください。

行動説明: ${transformedActivity}

## 受信者: ${senderName}
## 敬語レベル: ${replyFormat.honorifics}
## 出力形式: ${replyFormat.format}

## 重要な制約
1. 行動説明の内容を使って、なぜ返信が遅れそうかを説明する
2. 敬語レベルは ${replyFormat.format} の形式を厳守する
3. 簡潔で自然な日本語にする（20-50文字程度）
4. 行動の内容は保持しつつ、返信遅延の理由として適切に表現する
5. 必ず謝罪表現を含める（申し訳ございません、ごめんなさい、ごめん等）

## 生成例
${replyFormat.examples.join('\n')}

## 指示
上記の行動説明を基に、${senderName}向けの「${replyFormat.format}」形式で返信メッセージを生成してください。
メッセージ本文のみを出力し、説明や思考過程は不要です。

返信メッセージ:`;
}

